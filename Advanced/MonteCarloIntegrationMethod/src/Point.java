public class Point { //Класс точка. Экземпляром данного класса будет точка с координатами x и у
    private double x; // объявляем координаты точки
    private double y;

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}


